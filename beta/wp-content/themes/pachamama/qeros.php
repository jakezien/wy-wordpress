      <p>Q&rsquo;eros live a hardworking life immersed in nature: they perform offerings to <i>Pacha Mama</i>, Mother Earth, and to the <i>Apus</i>, mountain spirits, in exchange for the well being of their animals, crops and themselves.</p>
    </div>
    </div class="grid">
  </section>
  
  <section id="portrait" class="img-placeholder"></section>
  <section id="portrait-caption" class="caption">
