        <p>The Q&rsquo;eros Nation has been designated by Peru&rsquo;s Ministry of Culture a &ldquo;national living cultural patrimony&rdquo; for the continuity of its ancient Andean traditions.</p>
        <br/>
        <p>Considered to be the &ldquo;&uacute;ltimo ayllu inka,&rdquo; or the last Incan community of Peru, the Q&rsquo;eros strive to preserve and promote their indigenous ethnic and cultural identity.</p>
      </div>
    </div>
