      <p>Worldview concepts of <i>ayni</i>, the importance of reciprocal sharing, and <i>animu</i>, awareness of an animated essence in all things, shape their interactions with each other and their environment. Love, joy, perseverance, intrinsic integrity, and the ability to &ldquo;simply just be&rdquo; suffuse their daily lives.</p>
    </div>
    </div class="grid">
  </section>

